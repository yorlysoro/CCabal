#include <stdio.h>

int main(void)
{
	printf("10 + 3 = %hd\n", 10 + 3);
	printf("10 - 3 = %hd\n", 10 - 3);
	printf("10 / 3 = %hd\n", 10 / 3);
	printf("10 / 3 = %f\n", 10 / 3);
	printf("10 / 3 = %f\n", 10 / 3.0);
	printf("10 * 3.5  = %hd\n", 10 * 3.5);
	printf("10 * 3.5 = %f\n", 10 * 3.5);
	printf("10 %% 3 = %hd\n", 10 % 3);
	printf("Listo.\n");
	return 0;
}
